package com.mx.edifact.model;


public class CfdiParametros {

	private int parametro_id;
	private String parametro_llave;
	private String parametro_valor;

	public int getParametro_id() {
		return parametro_id;
	}

	public void setParametro_id(int parametro_id) {
		this.parametro_id = parametro_id;
	}

	public String getParametro_llave() {
		return parametro_llave;
	}

	public void setParametro_llave(String parametro_llave) {
		this.parametro_llave = parametro_llave;
	}

	public String getParametro_valor() {
		return parametro_valor;
	}

	public void setParametro_valor(String parametro_valor) {
		this.parametro_valor = parametro_valor;
	}

}
